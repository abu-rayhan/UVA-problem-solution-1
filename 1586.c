#include<stdio.h>

int main(){
    int t,l,i,c;
    double d;
    char s[80];
    scanf("%d",&t);
    while(t--){
        scanf("%s",s);
        l=strlen(s);
        d=0;
        for(i=0;i<l;i++){
            if(s[i]=='C'){
                if((s[i+1]>='1'&&s[i+1]<='9')&&(s[i+2]>='1'&&s[i+2]<='9')){
                    c=(s[i+1]-'0')*10+s[i+2]-'0';
                    d+=c*12.01;
                }
                else if((s[i+1]>='2'&&s[i+1]<='9')){
                    c=s[i+1]-'0';
                    d+=c*12.01;
                }
                else
                d+=12.01;
            }
            else if(s[i]=='H'){
                if((s[i+1]>='1'&&s[i+1]<='9')&&(s[i+2]>='1'&&s[i+2]<='9')){
                    c=(s[i+1]-'0')*10+s[i+2]-'0';
                    d+=c*1.008;
                }
                else if(s[i+1]>='2'&&s[i+1]<='9'){
                    d+=(s[i+1]-'0')*1.008;
                }
                else d+=1.008;
            }
            else if(s[i]=='O'){
                if((s[i+1]>='1'&&s[i+1]<='9')&&(s[i+2]>='1'&&s[i+2]<='9')){
                    c=(s[i+1]-'0')*10+s[i+2]-'0';
                    d+=c*16.0;
                }
                else if(s[i+1]>='2'&&s[i+1]<='9'){
                    d+=(s[i+1]-'0')*16.0;
                }
                else d+=16.0;
            }
            else if(s[i]=='N'){
                if((s[i+1]>='1'&&s[i+1]<='9')&&(s[i+2]>='1'&&s[i+2]<='9')){
                    c=(s[i+1]-'0')*10+s[i+2]-'0';
                    d+=c*14.01;
                }
               else if(s[i+1]>='2'&&s[i+1]<='9'){
                    d+=(s[i+1]-'0')*14.01;
                }
                else d+=14.01;
            }
        }
        printf("%.3lf\n",d);
    }
    return 0;
}
